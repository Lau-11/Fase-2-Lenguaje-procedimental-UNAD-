 Manejando los posibles errores (por ejemplo, intentar insertar un cliente con un email duplicado). Implementar manejo de excepciones para evitar la interrupción del proceso.
 */
CREATE OR REPLACE PROCEDURE insertar_cliente (
        p_email_address IN VARCHAR2,
        p_full_name IN VARCHAR2
    ) IS -- Se mapea el error ORA-00001 (Violación de restricción única)
    e_email_duplicado EXCEPTION;
PRAGMA EXCEPTION_INIT(e_email_duplicado, -1);
BEGIN
INSERT INTO customers (email_address, full_name)
VALUES (p_email_address, p_full_name);
COMMIT;
DBMS_OUTPUT.PUT_LINE('Cliente registrado exitosamente.');
EXCEPTION
WHEN e_email_duplicado THEN DBMS_OUTPUT.PUT_LINE(
    'Error: El correo electrónico ya está registrado.'
);
ROLLBACK;
WHEN OTHERS THEN DBMS_OUTPUT.PUT_LINE('Error inesperado: ' || SQLERRM);
ROLLBACK;
END;
