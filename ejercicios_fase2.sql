/*
 9. Crear un cursor explícito que liste todos los productos de una tienda específica y calcule el valor total del inventario
 
 Se debe crear un procedimiento que use cursores para obtener todos los productos de una tienda y calcular el valor total del inventario en esa tienda.
 */
CREATE OR REPLACE PROCEDURE reporte_inventario_tienda (p_store_id NUMBER) IS CURSOR c_productos IS
SELECT p.product_name,
    i.product_inventory,
    p.unit_price,
    (i.product_inventory * p.unit_price) AS valor_producto
FROM inventory i
    JOIN products p ON i.product_id = p.product_id
WHERE i.store_id = p_store_id;
v_total_inventario NUMBER := 0;
BEGIN DBMS_OUTPUT.PUT_LINE('--- Inventario Tienda ' || p_store_id || ' ---');
FOR r_producto IN c_productos LOOP DBMS_OUTPUT.PUT_LINE(
    r_producto.product_name || ' | Cantidad: ' || r_producto.product_inventory
);
v_total_inventario := v_total_inventario + r_producto.valor_producto;
END LOOP;
DBMS_OUTPUT.PUT_LINE('VALOR TOTAL: $' || v_total_inventario);
END;
