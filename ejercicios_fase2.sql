-- Creación de la tabla de historial
CREATE TABLE inventory_history (
    history_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    product_id NUMBER,
    store_id NUMBER,
    old_inventory NUMBER,
    new_inventory NUMBER,
    change_date DATE DEFAULT SYSDATE
);
-- Creación del Trigger
CREATE OR REPLACE TRIGGER trg_inventory_changes
AFTER
UPDATE OF product_inventory ON inventory FOR EACH ROW BEGIN IF :OLD.product_inventory != :NEW.product_inventory THEN
INSERT INTO inventory_history (
        product_id,
        store_id,
        old_inventory,
        new_inventory
    )
VALUES (
        :OLD.product_id,
        :OLD.store_id,
        :OLD.product_inventory,
        :NEW.product_inventory
    );
END IF;
END;
